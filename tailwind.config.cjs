// Custom plugin that adds each Tailwind color as a global CSS variable,
// e.g. var(--gray-200); required by AceternityUI
// https://ui.aceternity.com/components
function plgVariablesForColors({ addBase, theme }) {
  let allColors = flattenColorPalette(theme('colors'));
  let newVars = Object.fromEntries(
    Object.entries(allColors).map(([key, val]) => [`--${key}`, val])
  );

  addBase({
    ':root': newVars,
  });
}
